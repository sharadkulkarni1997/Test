#assignment operators
a = 10
b = 5
c = 0
c += a  # c = c + a
print("After c += a, c =", c)
c -= b  # c = c - b
print("After c -= b, c =", c)
c *= a  # c = c * a
print("After c *= a, c =", c)
c /= b  # c = c / b
print("After c /= b, c =", c)
c %= a  # c = c % a
print("After c %= a, c =", c)
c **= 2  # c = c ** 2 "to the power of"
print("After c **= 2, c =", c)
c //= 3  # c = c // 3
print("After c //= 3, c =", c)

